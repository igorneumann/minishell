#include "minishell.h"

int	cleanfds(t_cmd *cmd, int i)
{
	if ((i == 1 || i == 3) && cmd->in_fd)
		close(cmd->in_fd);
	if ((i == 2 || i == 3) && cmd->out_fd)
		close(cmd->out_fd);
	return (1);
}
