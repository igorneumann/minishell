#include "libft.h"

int	ft_isprint(int c)
{
	if (32 <= c && c <= 126)
		return (1);
	else if (c == 128 || (130 <= c && c <= 140))
		return (1);
	else if (c == 142 || (145 <= c && c <= 156))
		return (1);
	else if (c == 158 || (161 <= c && c <= 172))
		return (1);
	else if (c == 159 || (174 <= c && c <= 255))
		return (1);
	else if (c == -76 || c == -88 || c == -30)
		return (1);
	return (0);
}
